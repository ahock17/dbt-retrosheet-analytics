CREATE TABLE IF NOT EXISTS stage.park_code (
        parkid char(5) primary key,
        name varchar(200),
        aka varchar(200),
        city varchar(200),
        state varchar(10),
        start_date date,
        end_date date,
        league char(2),
        notes varchar(500)
);

CREATE TABLE IF NOT EXISTS stage.people (
        id char(8) primary key,
        last_name varchar(200),
        first_name varchar(200),
        play_debut date,
        mgr_debut date,
        coach_debut date,
        ump_debut date
);

CREATE TABLE IF NOT EXISTS stage.teams (
        team_abbr char(3),
        league char(2),
        city varchar(200),
        nickname varchar(200),
        first_year int,
        last_year int,
        PRIMARY KEY (team_abbr, league)
);

